package kr.co.solproject.qnabbs;

public class QnaDTO {
	private int qnano;
	private String id;
	private String subject;
	private String content;
	private String passwd;
	private int readcnt;
	private String regdt;
	private String replyok;
	private String qnashow;
	
	public int getQnano() {
		return qnano;
	}
	public void setQnano(int qnano) {
		this.qnano = qnano;
	}
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getSubject() {
		return subject;
	}
	public void setSubject(String subject) {
		this.subject = subject;
	}
	public String getContent() {
		return content;
	}
	public void setContent(String content) {
		this.content = content;
	}
	public String getPasswd() {
		return passwd;
	}
	public void setPasswd(String passwd) {
		this.passwd = passwd;
	}
	public int getReadcnt() {
		return readcnt;
	}
	public void setReadcnt(int readcnt) {
		this.readcnt = readcnt;
	}
	public String getRegdt() {
		return regdt;
	}
	public void setRegdt(String regdt) {
		this.regdt = regdt;
	}
	public String getReplyok() {
		return replyok;
	}
	public void setReplyok(String replyok) {
		this.replyok = replyok;
	}
	public String getQnashow() {
		return qnashow;
	}
	public void setQnashow(String qnashow) {
		this.qnashow = qnashow;
	}
	
	
}
